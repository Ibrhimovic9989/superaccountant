/**
 * Pick `count` random facts from the pool. Stable for SSR — uses a seed.
 */
export function pickFacts(locale: 'en' | 'ar', count = 5, seed = Date.now()): string[] {
  const pool = ACCOUNTING_FACTS.map((f) => f[locale])
  // Lightweight Fisher-Yates with a seeded PRNG so SSR/CSR agree per seed.
  let s = seed >>> 0
  const next = () => {
    s = (s * 1664525 + 1013904223) >>> 0
    return s / 0xffffffff
  }
  const arr = [...pool]
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(next() * (i + 1))
    ;[arr[i]!, arr[j]!] = [arr[j]!, arr[i]!]
  }
  return arr.slice(0, Math.min(count, arr.length))
}
