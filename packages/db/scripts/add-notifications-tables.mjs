// One-shot: create the Phase 1 engagement / notification spine.
//
// Four tables, all raw SQL (not declared in schema.prisma) — follows the
// loyalty / careers / cohort pattern. The notifications context reads /
// writes them via prisma.$queryRaw / $executeRaw, so no schema.prisma
// changes are needed and no prisma generate cycle.
//
// Schema:
//   NotificationPreference   — per-user opt-in matrix, one row per
//                              (userId, channel, category).
//   ScheduledNotification    — queue. Scheduler picks rows where
//                              status='pending' AND scheduledAt <= now.
//   NotificationDelivery     — audit log. One row per dispatch attempt
//                              (delivered, stubbed, or failed).
//   NotificationInbox        — the in-app bell-dropdown inbox (Phase 2
//                              frontend will render this).
//
// IDs are TEXT primary keys generated by the app layer (cuid / randomUUID),
// matching the loyalty + cohort tables convention.

import { readFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __d = dirname(fileURLToPath(import.meta.url))
for (const line of readFileSync(resolve(__d, '../.env'), 'utf8').split('\n')) {
  const m = /^([A-Z0-9_]+)=(.*)$/.exec(line.trim())
  if (m && !process.env[m[1]]) process.env[m[1]] = m[2].replace(/^"|"$/g, '')
}
const { PrismaClient } = await import('@prisma/client')
const p = new PrismaClient()

// ── NotificationPreference ─────────────────────────────────────
// One row per (user, channel, category). Defaults to opted-in so that
// the absence of a row means "deliver" rather than "withhold" — Phase 2
// settings UI lets the user flip flags off and that materialises rows
// for whatever they explicitly opt out of.
await p.$executeRawUnsafe(`
  CREATE TABLE IF NOT EXISTS "NotificationPreference" (
    "id" TEXT PRIMARY KEY,
    "userId" TEXT NOT NULL REFERENCES "IdentityUser"("id") ON DELETE CASCADE,
    "channel" TEXT NOT NULL CHECK ("channel" IN ('in_app','email','push','whatsapp')),
    "category" TEXT NOT NULL CHECK ("category" IN ('class_reminder','daily_nudge','streak_at_risk','weekly_progress','system')),
    "optedIn" BOOLEAN NOT NULL DEFAULT TRUE,
    "frequencyHint" TEXT NOT NULL DEFAULT 'immediate' CHECK ("frequencyHint" IN ('immediate','daily_digest','weekly')),
    "createdAt" TIMESTAMP(3) NOT NULL DEFAULT NOW(),
    "updatedAt" TIMESTAMP(3) NOT NULL DEFAULT NOW()
  )
`)
console.log('✓ NotificationPreference table')

await p.$executeRawUnsafe(`
  CREATE UNIQUE INDEX IF NOT EXISTS "NotificationPreference_user_channel_category_idx"
  ON "NotificationPreference" ("userId","channel","category")
`)
console.log('✓ NotificationPreference unique (userId, channel, category)')

// ── ScheduledNotification ──────────────────────────────────────
// The queue. dispatchDue() picks pending rows whose scheduledAt is in
// the past. Status transitions:
//   pending → sent             (adapter delivered OR stubbed — both
//                                count as fulfilled work for the queue)
//   pending → failed           (adapter threw)
//   pending → skipped_opt_out  (user opted out of (channel, category))
//   pending → cancelled        (manually cancelled, e.g. class moved)
await p.$executeRawUnsafe(`
  CREATE TABLE IF NOT EXISTS "ScheduledNotification" (
    "id" TEXT PRIMARY KEY,
    "userId" TEXT NOT NULL REFERENCES "IdentityUser"("id") ON DELETE CASCADE,
    "channel" TEXT NOT NULL,
    "category" TEXT NOT NULL,
    "payload" JSONB NOT NULL,
    "scheduledAt" TIMESTAMP(3) NOT NULL,
    "status" TEXT NOT NULL DEFAULT 'pending' CHECK ("status" IN ('pending','sent','failed','cancelled','skipped_opt_out')),
    "sentAt" TIMESTAMP(3),
    "error" TEXT,
    "createdAt" TIMESTAMP(3) NOT NULL DEFAULT NOW()
  )
`)
console.log('✓ ScheduledNotification table')

// Partial index for the hot path: dispatcher picks only pending rows.
await p.$executeRawUnsafe(`
  CREATE INDEX IF NOT EXISTS "ScheduledNotification_due_idx"
  ON "ScheduledNotification" ("status","scheduledAt")
  WHERE "status" = 'pending'
`)
await p.$executeRawUnsafe(`
  CREATE INDEX IF NOT EXISTS "ScheduledNotification_user_idx"
  ON "ScheduledNotification" ("userId","createdAt" DESC)
`)
console.log('✓ ScheduledNotification indexes')

// ── NotificationDelivery ───────────────────────────────────────
// Append-only audit log of every dispatch attempt.
//   delivered → the channel adapter completed end-to-end
//   stubbed   → the adapter is a placeholder (push/whatsapp before
//               FCM/Meta are wired). Counts as work done for the
//               queue but tagged so we can report on it.
//   failed    → adapter threw; error captured.
await p.$executeRawUnsafe(`
  CREATE TABLE IF NOT EXISTS "NotificationDelivery" (
    "id" TEXT PRIMARY KEY,
    "scheduledNotificationId" TEXT REFERENCES "ScheduledNotification"("id") ON DELETE SET NULL,
    "userId" TEXT NOT NULL REFERENCES "IdentityUser"("id") ON DELETE CASCADE,
    "channel" TEXT NOT NULL,
    "deliveredAt" TIMESTAMP(3) NOT NULL DEFAULT NOW(),
    "providerMessageId" TEXT,
    "status" TEXT NOT NULL CHECK ("status" IN ('delivered','stubbed','failed')),
    "error" TEXT
  )
`)
await p.$executeRawUnsafe(`
  CREATE INDEX IF NOT EXISTS "NotificationDelivery_user_idx"
  ON "NotificationDelivery" ("userId","deliveredAt" DESC)
`)
console.log('✓ NotificationDelivery table + index')

// ── NotificationInbox ──────────────────────────────────────────
// What the bell-dropdown in apps/web renders in Phase 2. Each row is
// the in-app surface of a delivery. We write here from the in-app
// channel adapter; the row predates any read by the user (readAt
// stays null until they open it).
await p.$executeRawUnsafe(`
  CREATE TABLE IF NOT EXISTS "NotificationInbox" (
    "id" TEXT PRIMARY KEY,
    "userId" TEXT NOT NULL REFERENCES "IdentityUser"("id") ON DELETE CASCADE,
    "title" TEXT NOT NULL,
    "body" TEXT,
    "link" TEXT,
    "category" TEXT NOT NULL,
    "readAt" TIMESTAMP(3),
    "createdAt" TIMESTAMP(3) NOT NULL DEFAULT NOW()
  )
`)
await p.$executeRawUnsafe(`
  CREATE INDEX IF NOT EXISTS "NotificationInbox_user_idx"
  ON "NotificationInbox" ("userId","readAt","createdAt" DESC)
`)
console.log('✓ NotificationInbox table + index')

await p.$disconnect()
console.log('\nNotifications Phase 1 tables ready.')
